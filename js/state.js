const RANKS = [
  { min: 0,    tier: 'IV',    label: 'RECRUIT COMMANDER' },
  { min: 150,  tier: 'III',   label: 'COMMANDER' },
  { min: 500,  tier: 'II',    label: 'SENIOR COMMANDER' },
  { min: 1100, tier: 'I',     label: 'ELITE COMMANDER' },
  { min: 2000, tier: 'PRIME', label: 'COMMANDER PRIME' },
  { min: 3500, tier: 'APEX',  label: 'COMMANDER APEX' }
];

const XP_TABLE = { LOW: 40, MED: 70, HIGH: 110, EXTREME: 180, '???': 250 };

const DieselState = {
  KEY: 'diesel_system_state_v1',

  defaults(){
    return {
      lockdown: false,
      broadcastCount: 0,
      claimedMissions: [],
      completedMissions: [],
      recruited: [],
      xp: 0,
      spotlight: false,
      soundOn: true,
      alerts: [],          // dynamic alerts generated by actions
      readAlerts: [],       // ids of read alerts (static + dynamic)
      messageReplies: {},   // { threadId: [ {who:'me'|'them', txt, t} ] }
      readMessages: [],
      accent: 'green',        // 'green' | 'cyan' | 'crimson' — UI accent theme
      transactions: [],       // dynamic wallet transactions generated by completed missions
      achievements: [],        // ids of unlocked achievements
      dispatches: [],          // { id, kind:'node'|'territory'|'operation', target, text, code, ts, status:'pending'|'done', reply, doneCode }
      firewallBlocks: 0,       // counter for FIREWALL sweeps run from the map
      territories: null,       // lazy-seeded control map, see TERRITORY_DEFAULTS
      opProgress: {},          // { opId: { phase:0, done:false } }
      heat: 0,                 // 0-100 rival attention/exposure meter
      lastVisit: Date.now()    // real-world timestamp used by runWorldTick()
    };
  },

  parseCurrency(str){
    if(typeof str !== 'string') return 0;
    const n = parseFloat(str.replace(/[^0-9.]/g, ''));
    return isNaN(n) ? 0 : n;
  },

  load(){
    let s;
    try{
      const raw = localStorage.getItem(this.KEY);
      s = raw ? JSON.parse(raw) : {};
    }catch(e){ s = {}; }
    // merge with defaults so old saves gain new fields safely
    return Object.assign(this.defaults(), s);
  },

  save(state){
    try{ localStorage.setItem(this.KEY, JSON.stringify(state)); }catch(e){}
  },

  get(){ return this.load(); },

  // ---------- core toggles ----------
  toggleLockdown(){
    const s = this.load();
    s.lockdown = !s.lockdown;
    if(s.lockdown) s.heat = Math.max(0, s.heat - 12); // going dark cools exposure immediately
    this.save(s);
    this.addAlert('security', s.lockdown ? '&#128274;' : '&#128275;',
      s.lockdown ? 'System lockdown engaged — new connections held at the gate' : 'Lockdown disengaged — node open to new connections');
    const newAchievements = this.checkAchievements();
    return { lockdown: s.lockdown, newAchievements, heat: s.heat };
  },

  broadcast(){
    const s = this.load();
    s.broadcastCount++;
    this.save(s);
    this.addAlert('growth', '&#128227;', `Broadcast #${s.broadcastCount} reached all 15 units with zero drop-off`);
    const newAchievements = this.checkAchievements();
    return { count: s.broadcastCount, newAchievements };
  },

  // ---------- XP / rank ----------
  rankFor(xp){
    let current = RANKS[0];
    for(const r of RANKS){ if(xp >= r.min) current = r; }
    return current;
  },

  nextRank(xp){
    return RANKS.find(r => r.min > xp) || null;
  },

  addXp(amount){
    const s = this.load();
    const before = this.rankFor(s.xp);
    s.xp += amount;
    const after = this.rankFor(s.xp);
    this.save(s);
    const leveledUp = after.tier !== before.tier;
    if(leveledUp){
      this.addAlert('growth', '&#127942;', `Rank up — now ${after.label} (TIER ${after.tier})`);
    }
    return { xp: s.xp, before, after, leveledUp, gained: amount };
  },

  // ---------- missions ----------
  claimMission(id){
    const s = this.load();
    if(!s.claimedMissions.includes(id)) s.claimedMissions.push(id);
    this.save(s);
    return s;
  },

  completeMission(id, diff, reward, title){
    const s = this.load();
    const alreadyDone = s.completedMissions.includes(id);
    if(!alreadyDone) s.completedMissions.push(id);
    s.claimedMissions = s.claimedMissions.filter(m => m !== id);
    this.save(s);
    const gained = XP_TABLE[diff] || 50;
    const result = this.addXp(gained);
    this.addAlert('mission', '&#9989;', `Mission marked complete — +${gained} XP earned`);

    let tx = null;
    const amount = this.parseCurrency(reward);
    if(!alreadyDone && amount > 0){
      tx = this.addTransaction({
        title: (title || id) + ' — CONTRACT PAYOUT',
        desc: 'Contract closed and settled following mission completion.',
        amount, dir: 'in', tag: 'mission'
      });
    }
    const newAchievements = this.checkAchievements();
    return { ...result, tx, newAchievements };
  },

  isClaimed(id){ return this.load().claimedMissions.includes(id); },
  isCompleted(id){ return this.load().completedMissions.includes(id); },

  // ---------- wallet / transactions ----------
  addTransaction(tx){
    const s = this.load();
    const entry = Object.assign({
      id: 'tx_' + Date.now() + '_' + Math.floor(Math.random()*1000),
      date: 'just now',
      ts: Date.now()
    }, tx);
    s.transactions.unshift(entry);
    this.save(s);
    return entry;
  },

  walletTotals(seedTransactions, startingBalance){
    const all = [...this.load().transactions, ...(seedTransactions || [])];
    let totalIn = 0, totalOut = 0, largest = 0;
    all.forEach(t => {
      if(t.dir === 'in'){ totalIn += t.amount; if(t.amount > largest) largest = t.amount; }
      else totalOut += t.amount;
    });
    const balance = (startingBalance || 0) + totalIn - totalOut;
    return { balance, totalIn, totalOut, largest, all };
  },

  // ---------- achievements ----------
  checkAchievements(){
    const s = this.load();
    const unlocked = [];
    if(typeof ACHIEVEMENTS === 'undefined') return unlocked;
    ACHIEVEMENTS.forEach(a => {
      if(!s.achievements.includes(a.id) && a.test(s)){
        s.achievements.push(a.id);
        unlocked.push(a);
        this.addAlert('growth', a.icon || '&#127942;', `Achievement unlocked — ${a.name}`);
      }
    });
    if(unlocked.length) this.save(s);
    return unlocked;
  },

  // ---------- alerts ----------
  addAlert(kind, icon, msg){
    const s = this.load();
    const alert = { id: 'dyn_' + Date.now() + '_' + Math.floor(Math.random()*1000), kind, icon, msg, ts: Date.now() };
    s.alerts.unshift(alert);
    s.alerts = s.alerts.slice(0, 40); // cap history
    this.save(s);
    return alert;
  },

  markAlertsRead(ids){
    const s = this.load();
    ids.forEach(id => { if(!s.readAlerts.includes(id)) s.readAlerts.push(id); });
    this.save(s);
  },

  // ---------- messages ----------
  addReply(threadId, who, txt){
    const s = this.load();
    if(!s.messageReplies[threadId]) s.messageReplies[threadId] = [];
    s.messageReplies[threadId].push({ who, txt, t: Date.now() });
    this.save(s);
  },

  markMessageRead(threadId){
    const s = this.load();
    if(!s.readMessages.includes(threadId)) s.readMessages.push(threadId);
    this.save(s);
  },

  markAllMessagesRead(ids){
    const s = this.load();
    ids.forEach(id => { if(!s.readMessages.includes(id)) s.readMessages.push(id); });
    this.save(s);
  },

  // ---------- sound ----------
  toggleSound(){
    const s = this.load();
    s.soundOn = !s.soundOn;
    this.save(s);
    return s.soundOn;
  },

  // ---------- appearance ----------
  setAccent(accent){
    const s = this.load();
    s.accent = accent;
    this.save(s);
    if(window.DieselTheme) window.DieselTheme.apply(accent);
    return s;
  },

  // ---------- reset ----------
  resetProgress(){
    try{ localStorage.removeItem(this.KEY); }catch(e){}
  },

  // ---------- export / import ----------
  exportState(){
    return JSON.stringify(this.load(), null, 2);
  },

  importState(jsonText){
    try{
      const parsed = JSON.parse(jsonText);
      const merged = Object.assign(this.defaults(), parsed);
      this.save(merged);
      return true;
    }catch(e){
      return false;
    }
  },

  // ---------- map: dispatches (commands / org messages sent from the globe) ----------
  DISPATCH_DELAY_MS: 3 * 60 * 1000, // 3 minutes

  genCode(){
    const hex = () => Math.floor(Math.random()*0xffff).toString(16).toUpperCase().padStart(4,'0');
    return `${hex()}-${hex()}`;
  },

  addDispatch(target, text, kind, operative){
    const s = this.load();
    const entry = {
      id: 'dsp_' + Date.now() + '_' + Math.floor(Math.random()*1000),
      kind: kind || 'node',
      target, text,
      operative: operative || null,
      code: this.genCode(),
      ts: Date.now(),
      status: 'pending',
      reply: null,
      doneCode: null
    };
    s.dispatches.unshift(entry);
    s.dispatches = s.dispatches.slice(0, 60);
    this.save(s);
    return entry;
  },

  // resolves any pending dispatch whose 3-minute window has elapsed; returns newly resolved entries
  resolveDueDispatches(replyBank){
    const s = this.load();
    const justResolved = [];
    const now = Date.now();
    s.dispatches.forEach(d => {
      if(d.status === 'pending' && now - d.ts >= this.DISPATCH_DELAY_MS){
        d.status = 'done';
        d.doneCode = this.genCode();
        const bank = (replyBank && replyBank[d.target]) || ['Acknowledged. Directive carried out, no resistance met.'];
        d.reply = bank[Math.floor(Math.random()*bank.length)];

        if(d.kind === 'territory'){
          const result = this._shiftTerritory(s, d.target, d.operative);
          d.reply = result.text;
          this.addAlert('growth', result.success ? '&#127760;' : '&#9888;', result.alertMsg);
        } else if(d.kind === 'operation'){
          const result = this._advanceOperation(s, d.target, d.operative);
          d.reply = result.text;
          if(result.finished) this.addAlert('growth', '&#127942;', result.alertMsg);
          else this.addAlert('mission', '&#128225;', result.alertMsg);
        } else {
          this.addAlert('mission', '&#128225;', `${d.target} confirmed execution of your directive`);
        }
        justResolved.push(d);
      }
    });
    if(justResolved.length) this.save(s);
    return justResolved;
  },

  getDispatches(){ return this.load().dispatches; },

  // ---------- map: territory control (ground system) ----------
  TERRITORY_DEFAULTS: {
    NA: { name:'North America', diesel:35, rival:20, neutral:45 },
    SA: { name:'South America', diesel:55, rival:15, neutral:30 },
    EU: { name:'Europe',        diesel:70, rival:10, neutral:20 },
    AF: { name:'Africa',        diesel:25, rival:15, neutral:60 },
    AS: { name:'Asia',          diesel:40, rival:35, neutral:25 },
    OC: { name:'Oceania',       diesel:60, rival:5,  neutral:35 }
  },

  // per-region threat score (0-100), derived from the strongest rival faction operating in that region.
  // drives both ground-op success odds and passive incursion strength/frequency — keep in sync with
  // the FACTIONS table in map.html if faction rosters change.
  REGION_THREAT: { NA:88, SA:57, EU:38, AF:61, AS:80, OC:41 },
  THREAT_CRITICAL: 75,

  // fires a one-time alert per region the first time rival presence crosses 50% in a CRITICAL-threat
  // region — call this after any territory-affecting action (dispatch tick, ground op, passive incursion).
  checkFactionThreatAlerts(){
    const s = this.load();
    if(!s.territories) return;
    if(!s.threatAlerted) s.threatAlerted = {};
    let changed = false;
    Object.keys(s.territories).forEach(code => {
      const t = s.territories[code];
      const regionThreat = this.REGION_THREAT[code] || 40;
      if(regionThreat >= this.THREAT_CRITICAL && t.rival >= 50 && !s.threatAlerted[code]){
        s.threatAlerted[code] = true;
        changed = true;
        this.addAlert('security', '&#128680;', `CRITICAL — a high-threat faction now holds over half of ${t.name}. Recommend prioritizing a ground operation there.`);
      } else if((regionThreat < this.THREAT_CRITICAL || t.rival < 50) && s.threatAlerted[code]){
        delete s.threatAlerted[code]; // reset so it can fire again if it spikes back up later
        changed = true;
      }
    });
    if(changed) this.save(s);
  },

  getTerritories(){
    const s = this.load();
    if(!s.territories){
      s.territories = JSON.parse(JSON.stringify(this.TERRITORY_DEFAULTS));
      this.save(s);
    }
    return s.territories;
  },

  launchGroundOp(code, operativeId){
    const t = this.getTerritories()[code];
    if(!t) return null;
    const lead = this._crewName(operativeId);
    const text = `Ground operation launched to contest ${t.name} and expand DIESEL influence.` + (lead ? ` ${lead} is leading the op.` : '');
    this.addHeat(this.HEAT_PER_GROUND_OP);
    return this.addDispatch(code, text, 'territory', operativeId);
  },

  _crewName(operativeId){
    if(!operativeId || typeof CREW === 'undefined') return null;
    const c = CREW.find(x => x.id === operativeId);
    return c ? c.nm : null;
  },

  _successChance(operativeId, fallback, heat){
    let base = fallback;
    if(operativeId && typeof CREW !== 'undefined'){
      const c = CREW.find(x => x.id === operativeId);
      if(c) base = Math.min(0.98, c.successRate / 100);
    }
    const heatPenalty = (heat || 0) / 400; // up to -0.25 at heat=100
    return Math.max(0.08, base - heatPenalty);
  },

  _shiftTerritory(s, code, operativeId){
    if(!s.territories) s.territories = JSON.parse(JSON.stringify(this.TERRITORY_DEFAULTS));
    const t = s.territories[code];
    if(!t) return { success:false, text:'Region unrecognized.', alertMsg:'Ground operation failed — unknown sector.' };
    const lead = this._crewName(operativeId);
    const leadNote = lead ? ` ${lead} led the op.` : '';
    const regionThreat = this.REGION_THREAT[code] || 40;
    const threatPenalty = regionThreat / 500; // up to -0.2 success chance at threat=100
    const success = Math.random() < Math.max(0.08, this._successChance(operativeId, 0.72, s.heat) - threatPenalty);
    const swing = 3 + Math.floor(Math.random()*7); // 3-9%
    if(success){
      const effectiveSwing = Math.max(2, swing - Math.round(regionThreat/40)); // strong factions blunt your gains
      t.diesel = Math.min(100, t.diesel + effectiveSwing);
      const takeFromRival = Math.min(t.rival, Math.ceil(effectiveSwing*0.6));
      t.rival -= takeFromRival;
      t.neutral = Math.max(0, 100 - t.diesel - t.rival);
      return {
        success:true,
        text:`Ground team secured new footing in ${t.name}. DIESEL influence now ${t.diesel}%.${leadNote}`,
        alertMsg:`Territory shift — DIESEL influence in ${t.name} rose to ${t.diesel}%`
      };
    } else {
      const pushback = Math.ceil(swing * (0.4 + regionThreat/250)); // stronger factions push back harder
      t.diesel = Math.max(0, t.diesel - Math.ceil(swing*0.5));
      t.rival = Math.min(100, t.rival + pushback);
      t.neutral = Math.max(0, 100 - t.diesel - t.rival);
      return {
        success:false,
        text:`Pushback in ${t.name} — rival presence held the line. DIESEL influence dipped to ${t.diesel}%.${leadNote}`,
        alertMsg:`Territory contest lost ground in ${t.name} — down to ${t.diesel}%`
      };
    }
  },

  // ---------- map: high-level global operations ----------
  OPERATIONS: [
    { id:'BLACKTIDE', name:'OPERATION BLACK TIDE', phases:['Recon the target network','Breach and hold access','Extract and go dark'], reward:260 },
    { id:'QUIETSTORM', name:'OPERATION QUIET STORM', phases:['Map rival command chain','Turn a low-level contact','Sever their coordination'], reward:300 },
    { id:'IRONVEIL', name:'OPERATION IRON VEIL', phases:['Harden all active nodes','Plant false signals for rivals','Confirm full mesh integrity'], reward:220 }
  ],

  getOpProgress(){ return this.load().opProgress || {}; },

  launchOperationPhase(opId, operativeId){
    const op = this.OPERATIONS.find(o => o.id === opId);
    if(!op) return null;
    const s = this.load();
    const prog = s.opProgress[opId] || { phase:0, done:false };
    if(prog.done) return null;
    const phaseLabel = op.phases[prog.phase] || 'Final phase';
    const lead = this._crewName(operativeId);
    this.save(s);
    this.addHeat(this.HEAT_PER_OP_PHASE);
    return this.addDispatch(opId, `${op.name} — ${phaseLabel}` + (lead ? ` (led by ${lead})` : ''), 'operation', operativeId);
  },

  _advanceOperation(s, opId, operativeId){
    const op = this.OPERATIONS.find(o => o.id === opId);
    if(!s.opProgress) s.opProgress = {};
    if(!s.opProgress[opId]) s.opProgress[opId] = { phase:0, done:false };
    const prog = s.opProgress[opId];
    if(!op || prog.done) return { finished:false, text:'Operation already closed out.', alertMsg:'' };

    const lead = this._crewName(operativeId);
    const leadNote = lead ? ` ${lead} handled it personally.` : '';
    const completedPhase = op.phases[prog.phase];
    prog.phase++;
    if(prog.phase >= op.phases.length){
      prog.done = true;
      this.addXp(op.reward);
      return {
        finished:true,
        text:`Phase complete: "${completedPhase}".${leadNote} ${op.name} is fully closed out — clean.`,
        alertMsg:`${op.name} complete — +${op.reward} XP earned`
      };
    }
    const nextPhase = op.phases[prog.phase];
    return {
      finished:false,
      text:`Phase complete: "${completedPhase}".${leadNote} Next up: "${nextPhase}".`,
      alertMsg:`${op.name} — phase ${prog.phase} of ${op.phases.length} complete`
    };
  },

  // ---------- heat / exposure meter ----------
  HEAT_PER_GROUND_OP: 9,
  HEAT_PER_OP_PHASE: 6,
  HEAT_DECAY_PER_MIN: 0.55,       // passive decay, per real-world minute
  HEAT_DECAY_LOCKDOWN_MULT: 1.7,   // lockdown hides you faster

  addHeat(amount){
    const s = this.load();
    s.heat = Math.max(0, Math.min(100, s.heat + amount));
    this.save(s);
    if(s.heat >= 85){
      this.addAlert('security', '&#128680;', 'Exposure critical — rival counter-surveillance actively hunting your signal');
    } else if(s.heat >= 60 && s.heat - amount < 60){
      this.addAlert('security', '&#9888;', 'Heat rising — recent operations have drawn unwanted attention');
    }
    return s.heat;
  },

  heatTier(heat){
    if(heat >= 85) return { label: 'CRITICAL', cls: 'crit' };
    if(heat >= 60) return { label: 'HOT',      cls: 'hot'  };
    if(heat >= 30) return { label: 'ELEVATED', cls: 'elev' };
    return { label: 'DORMANT', cls: 'calm' };
  },

  // ---------- world tick: simulates time passing while the user was away ----------
  DEFAULT_REPLY_BANK: { },

  runWorldTick(replyBank){
    const s = this.load();
    const now = Date.now();
    if(!s.lastVisit){ s.lastVisit = now; this.save(s); return { awayMinutes: 0 }; }

    const elapsedMs = now - s.lastVisit;
    const elapsedMin = elapsedMs / 60000;
    if(elapsedMin < 1){ return { awayMinutes: 0 }; } // avoid churn on rapid page nav

    const cappedMin = Math.min(elapsedMin, 360); // simulate at most 6h of absence at once

    // passive heat decay
    const decayRate = this.HEAT_DECAY_PER_MIN * (s.lockdown ? this.HEAT_DECAY_LOCKDOWN_MULT : 1);
    s.heat = Math.max(0, s.heat - decayRate * cappedMin);

    // resolve anything that finished cooking while away
    this.save(s);
    this.resolveDueDispatches(replyBank || this.DEFAULT_REPLY_BANK);

    // passive rival incursion — only rolls if gone long enough, and only if territories exist
    // regions with stronger tracked factions (higher REGION_THREAT) get picked more often and hit harder
    let incursion = null;
    const s2 = this.load();
    if(s2.territories && cappedMin >= 5){
      const chance = Math.min(0.55, cappedMin / 240 + s2.heat / 300);
      if(Math.random() < chance){
        const codes = Object.keys(s2.territories);
        const weights = codes.map(c => this.REGION_THREAT[c] || 40);
        const totalW = weights.reduce((a,b) => a+b, 0);
        let roll = Math.random() * totalW;
        let code = codes[codes.length - 1];
        for(let i = 0; i < codes.length; i++){
          if(roll < weights[i]){ code = codes[i]; break; }
          roll -= weights[i];
        }
        const t = s2.territories[code];
        const regionThreat = this.REGION_THREAT[code] || 40;
        const swing = 2 + Math.floor(Math.random() * 5) + Math.round(regionThreat / 40);
        t.rival = Math.min(100, t.rival + swing);
        t.diesel = Math.max(0, t.diesel - Math.ceil(swing * 0.7));
        t.neutral = Math.max(0, 100 - t.diesel - t.rival);
        incursion = { code, name: t.name, swing };
        this.addAlert('security', '&#9889;', `While you were offline: rival forces pushed into ${t.name} — DIESEL influence dipped to ${t.diesel}%`);
      }
    }

    s2.lastVisit = now;
    this.save(s2);
    return { awayMinutes: Math.round(cappedMin), heatAfter: s2.heat, incursion };
  },

  // ---------- map: firewall sweep ----------
  triggerFirewall(){
    const s = this.load();
    const blocked = 4 + Math.floor(Math.random()*9); // 4-12 flavor "threats blocked" per sweep
    s.firewallBlocks += blocked;
    this.save(s);
    this.addAlert('security', '&#128737;', `Firewall sweep complete — ${blocked} intrusion attempts blocked at the perimeter`);
    return { blocked, total: s.firewallBlocks };
  }
};
